private final class ConsecutiveQuestionLLMClient: LLMClientProtocol, @unchecked Sendable {
    let providerKind: LLMProviderKind = .deepSeek
    var stageADelayByQuestion = [String: UInt64]()
    var stageBDelayByQuestion = [String: UInt64]()
    var hangingStageAQuestions = Set<String>()

    func testConnection(configuration: LLMProviderConfiguration) async throws -> LLMConnectionTestResult {
        LLMConnectionTestResult(success: true, message: "Mock OK", latencyMS: 0, models: [])
    }

    func chatCompletion(
        configuration: LLMProviderConfiguration,
        messages: [LLMChatMessage],
        responseFormat: LLMResponseFormat?,
        options: LLMRequestOptions
    ) async throws -> LLMChatResult {
        let questionKey = questionKey(from: messages)
        if let delay = stageBDelayByQuestion[questionKey] {
            try? await Task.sleep(nanoseconds: delay)
        }
        let content = fullCardJSON(for: questionKey)
        return LLMChatResult(
            content: content,
            modelName: "mock-model",
            providerKind: .deepSeek,
            providerName: "DeepSeek",
            baseURL: "",
            latencyMS: 100,
            isLocal: false,
            rawResponse: content
        )
    }

    func chatCompletionStream(
        configuration: LLMProviderConfiguration,
        messages: [LLMChatMessage],
        responseFormat: LLMResponseFormat?,
        options: LLMRequestOptions
    ) -> AsyncThrowingStream<String, Error> {
        let promptText = messages.map(\.content).joined(separator: "\n")
        let questionKey = questionKey(from: messages)
        let isStageB = promptText.contains("Return plain text sections only")
        let stageADelay = stageADelayByQuestion[questionKey] ?? 0
        let stageBDelay = stageBDelayByQuestion[questionKey] ?? 0
        let hangs = hangingStageAQuestions.contains(questionKey) && !isStageB

        return AsyncThrowingStream { continuation in
            let task = Task {
                if hangs {
                    try? await Task.sleep(nanoseconds: 60_000_000_000)
                    continuation.finish()
                    return
                }
                if isStageB {
                    if stageBDelay > 0 {
                        try? await Task.sleep(nanoseconds: stageBDelay)
                    }
                    for token in stageBSections(for: questionKey) {
                        if Task.isCancelled {
                            continuation.finish()
                            return
                        }
                        continuation.yield(token)
                    }
                    continuation.finish()
                    return
                }
                if stageADelay > 0 {
                    try? await Task.sleep(nanoseconds: stageADelay)
                }
                for token in ["I ", "can ", "walk ", "through ", "my ", "\(questionKey) ", "clearly."] {
                    if Task.isCancelled {
                        continuation.finish()
                        return
                    }
                    continuation.yield(token)
                }
                continuation.finish()
            }
            continuation.onTermination = { _ in task.cancel() }
        }
    }

    func listModels(configuration: LLMProviderConfiguration) async throws -> [LLMModelInfo] {
        []
    }

    private func questionKey(from messages: [LLMChatMessage]) -> String {
        let promptText = messages.map(\.content).joined(separator: "\n").lowercased()
        if promptText.contains("third project") {
            return "third project"
        }
        if promptText.contains("second project") {
            return "second project"
        }
        return "first project"
    }

    private func fullCardJSON(for questionKey: String) -> String {
        """
        {
          "strategy": "Project Walkthrough",
          "say_first": "I can explain my \(questionKey) in the LeoRover work by covering the autonomous object retrieval goal, ROS2 and YOLOv8 perception, navigation, manipulation, and the real robot result.",
          "key_points": ["LeoRover goal for \(questionKey)", "ROS2, YOLOv8, navigation, and manipulation", "Real robot result and learning"],
          "follow_up_ready": ["I can go deeper on robotics tradeoffs if helpful."],
          "confidence": 0.82,
          "caution": "None",
          "evidence_used": [],
          "risk_level": "low"
        }
        """
    }

    private func stageBSections(for questionKey: String) -> [String] {
        [
            "Strategy: Project Walkthrough\n",
            "Say First: I can explain my \(questionKey) in the LeoRover work by covering the autonomous object retrieval goal, ROS2 and YOLOv8 perception, navigation, manipulation, and the real robot result.\n",
            "Key Points:\n",
            "- LeoRover goal for \(questionKey)\n",
            "- ROS2, YOLOv8, navigation, and manipulation\n",
            "- Real robot result and learning\n",
            "Follow-up Ready:\n",
            "- I can go deeper on tradeoffs if helpful.\n",
            "Caution: None\n"
        ]
    }
