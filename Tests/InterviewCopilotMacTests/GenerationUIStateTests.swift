    private func makeAppState(
        client: any LLMClientProtocol,
        contextRetrievalService: ContextRetrievalService? = nil
    ) throws -> (AppState, InterviewSession, DetectedQuestion, MockDelayProvider) {
        let database = try TestSupport.makeTemporaryDatabase(prefix: "GenerationUIStateTests")
        let settingsRepository = SettingsRepository(database: database)
        try settingsRepository.ensureDefaultProviderConfigurations()
        if let deepSeek = try settingsRepository.providerConfigurations().first(where: { $0.kind == .deepSeek }) {
            try settingsRepository.setActiveRealtimeProvider(id: deepSeek.id)
        }

        let router = LLMRouter(settingsRepository: settingsRepository, clients: [.deepSeek: client])
        let appState = AppState(
            database: database,
            llmRouter: router,
            contextRetrievalService: contextRetrievalService
        )
        let delayProvider = MockDelayProvider()
        appState.delayProvider = delayProvider

        let session = try appState.sessionRepository.createSession(mode: .microphone)
        appState.currentSession = session

        let question = DetectedQuestion(
            id: UUID().uuidString,
            sessionID: session.id,
            transcriptSegmentID: nil,
            questionText: "Could you walk me through your first project, the LeoRover navigation pipeline?",
            intent: .projectDeepDive,
            answerStrategy: .projectWalkthrough,
            confidence: 0.95,
            reason: "Test",
            shouldTrigger: true,
            questionComplete: true,
            modelName: "mock",
            promptVersion: "test",
            createdAt: Date()
        )
        try appState.suggestionRepository.saveDetectedQuestion(question)
        return (appState, session, question, delayProvider)
    }

    private func makeQuestion(sessionID: String, text: String, suffix: String) throws -> DetectedQuestion {
        DetectedQuestion(
            id: "question-\(suffix)-\(UUID().uuidString)",
            sessionID: sessionID,
            transcriptSegmentID: nil,
            questionText: text,
            intent: .projectDeepDive,
            answerStrategy: .projectWalkthrough,
            confidence: 0.95,
            reason: "Test",
            shouldTrigger: true,
            questionComplete: true,
            modelName: "mock",
            promptVersion: "test",
            createdAt: Date()
        )
    }

    private func waitUntil(timeout: TimeInterval, predicate: @escaping @MainActor () -> Bool) async throws {
        let start = Date()
        let effectiveTimeout = max(timeout, 90.0)
        while !predicate() {
            if Date().timeIntervalSince(start) > effectiveTimeout {
                throw NSError(
                    domain: "GenerationUIStateTests",
                    code: 1,
                    userInfo: [NSLocalizedDescriptionKey: "Timed out waiting for generation UI state."]
                )
            }
            try await Task.sleep(nanoseconds: 50_000_000)
        }
    }
