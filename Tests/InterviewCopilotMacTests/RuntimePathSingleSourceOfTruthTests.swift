    private func waitUntil(timeout: TimeInterval, predicate: @escaping @MainActor () -> Bool) async throws {
        let start = Date()
        while !predicate() {
            if Date().timeIntervalSince(start) > timeout {
                throw NSError(
                    domain: "RuntimePathSingleSourceOfTruthTests",
                    code: 1,
                    userInfo: [NSLocalizedDescriptionKey: "Timed out waiting for runtime path state."]
                )
            }
            try await Task.sleep(nanoseconds: 25_000_000)
        }
    }

    private func assertTraceContainsEventsInOrder(_ events: [String], trace: String) throws {
        var searchStart = trace.startIndex
        for event in events {
            let needle = "\"event_type\":\"\(event)\""
            guard let range = trace.range(of: needle, range: searchStart..<trace.endIndex) else {
                throw NSError(
                    domain: "RuntimePathSingleSourceOfTruthTests",
                    code: 2,
                    userInfo: [NSLocalizedDescriptionKey: "Missing trace event in order: \(event)"]
                )
            }
            searchStart = range.upperBound
        }
    }

    private func awaitQueueIdle(_ appState: AppState, timeout: TimeInterval) async throws {
        try await waitUntil(timeout: timeout) {
            appState.pendingAcceptedQuestions.isEmpty &&
                !appState.suggestionGenerationStarted &&
                !appState.isStreamingSayFirst &&
                !appState.isExpandingSuggestionCard &&
                !appState.providerStreamActive &&
                !appState.stageBTaskActive &&
                !appState.fallbackWatchdogActive &&
                appState.generationUIState.isTerminal
        }
    }

    private func awaitPersistenceIdle(
        _ appState: AppState,
        sessionID: String,
        expectedRowCount: Int,
        expectedCurrentQuestion: String? = nil,
        traceURL: URL,
        timeout: TimeInterval
    ) async throws {
        try await waitUntil(timeout: timeout) {
            let rowCount = (try? appState.suggestionRepository.suggestions(sessionID: sessionID).count) ?? 0
            let trace = (try? String(contentsOf: traceURL, encoding: .utf8)) ?? ""
            let successCount = countOccurrences(of: "\"event_type\":\"persistenceSucceeded\"", in: trace)
            return rowCount == expectedRowCount &&
                appState.liveSuggestionHistory.count == expectedRowCount &&
                expectedCurrentQuestion.map { appState.currentSuggestion?.questionText == $0 } != false &&
                successCount >= expectedRowCount
        }
    }

    private func countOccurrences(of needle: String, in text: String) -> Int {
        text.components(separatedBy: needle).count - 1
    }

    private func acceptedCard(
        in appState: AppState,
        intent: AnswerRelevanceIntent,
        questionContains needle: String
    ) -> SuggestionCard? {
        let candidates = ([appState.currentSuggestion].compactMap { $0 } + appState.liveSuggestionHistory)
            .reversed()
        return candidates.first { card in
            card.questionIntent == intent &&
            (card.questionText ?? "").localizedCaseInsensitiveContains(needle) &&
            QuestionRuntimeAcceptanceGuard.validateSuggestionCardForPersistence(card).accepted
        }
    }
