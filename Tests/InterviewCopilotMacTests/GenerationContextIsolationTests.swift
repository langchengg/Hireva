    @Test
    func promptStartsWithExactCurrentQuestionAndLabelsBackground() {
        let current = makeQuestion("Why did the diffusion-based policy perform better than the autoregressive policy?")
        let snapshot = AnswerRelevancePolicy.promptSnapshot(
            question: current,
            context: mixedContext(),
            transcriptContext: "Interviewer: Which part of the LeoRover pipeline was most fragile when moving from a clean demo to real robot execution?",
            cvSummary: "Candidate has robotics, LeoRover, and VLA experience.",
            jdSummary: "Robotics role.",
            stage: .firstAnswer
        )

        #expect(snapshot.prompt.hasPrefix("""
        CURRENT QUESTION TO ANSWER:
        "\(current.questionText)"
        """))
        let questionRange = snapshot.prompt.range(of: "CURRENT QUESTION TO ANSWER:")
        let backgroundRange = snapshot.prompt.range(of: "BACKGROUND FROM EARLIER INTERVIEW")
        #expect(questionRange != nil)
        #expect(backgroundRange != nil)
        if let questionRange, let backgroundRange {
            #expect(questionRange.lowerBound < backgroundRange.lowerBound)
        }
        #expect(snapshot.prompt.localizedCaseInsensitiveContains("Previous transcript is background only and must not change the question."))
        #expect(snapshot.prompt.localizedCaseInsensitiveContains("Which part of the LeoRover pipeline was most fragile") == false)
    }

    @Test
    func firstAnswerWorthyQuestionIsNotSuppressedByStaleDuplicateMemory() async throws {
        let client = ContextIsolationLLMClient()
        let (appState, _, session) = try makeAppState(client: client)
        let questionText = "First, could you tell me a little bit about yourself and what brought you into robotics?"

        #expect(appState.isDuplicateAutoQuestion(questionText) == false)

        await appState.handleTranscriptSegment(systemAudioSegment(
            id: "first-stale-duplicate-segment",
            sessionID: session.id,
            text: questionText
        ))

        try await waitUntil(timeout: 8.0) {
            appState.detectedQuestionsInSessionCount == 1 &&
            appState.lastTranscriptQuestionGenerationTrace.generationTriggered &&
            appState.visibleAnswerExists
        }

        #expect(appState.lastTranscriptQuestionGenerationTrace.duplicateSuppressed == false)
        #expect(appState.currentSuggestion?.detectedQuestionID == appState.lastDetectedQuestion?.id)
        #expect(!appState.shouldShowBlockingAnswerSpinner)
    }

    @Test
    func fragilityThenDiffusionQuestionUsesCurrentSnapshotAndIntentContext() async throws {
        let client = ContextIsolationLLMClient()
        let retrieval = ContextIsolationRetrievalService()
        let (appState, _, session) = try makeAppState(client: client, retrievalService: retrieval)
        let first = try saveQuestion(
            "Which part of the pipeline was most fragile when moving from a clean demo to real robot execution?",
            sessionID: session.id,
            repository: appState.suggestionRepository,
            suffix: "fragility"
        )
        let second = try saveQuestion(
            "Why did the diffusion-based policy perform better than the autoregressive policy?",
            sessionID: session.id,
            repository: appState.suggestionRepository,
            suffix: "diffusion"
        )

        let firstTask = Task {
            try await appState.generateSuggestion(for: first, session: session, transcript: first.questionText, autoGenerated: true)
        }
        try await waitUntil(timeout: 8.0) {
            appState.currentSuggestion?.detectedQuestionID == first.id || appState.activeQuestionID == first.id
        }

        let secondTask = Task {
            try await appState.generateSuggestion(
                for: second,
                session: session,
                transcript: "Interviewer: \(first.questionText)\nInterviewer: \(second.questionText)",
                autoGenerated: true
            )
        }
        defer {
            firstTask.cancel()
            secondTask.cancel()
        }

        try await waitUntil(timeout: 8.0) {
            appState.currentSuggestion?.detectedQuestionID == second.id &&
            appState.currentQABinding.bindingStatus == .matched
        }

        let card = try #require(appState.currentSuggestion)
        #expect(card.questionText == second.questionText)
        #expect(card.promptQuestionText == second.questionText)
        #expect(card.sayFirst.localizedCaseInsensitiveContains("diffusion"))
        #expect(card.sayFirst.localizedCaseInsensitiveContains("autoregressive"))
        #expect(card.sayFirst.localizedCaseInsensitiveContains("smoother"))
        #expect(card.sayFirst.localizedCaseInsensitiveContains("robust"))
        #expect(card.sayFirst.localizedCaseInsensitiveContains("pipeline was most fragile") == false)
        #expect(appState.currentPromptContextPreviews.joined(separator: " ").localizedCaseInsensitiveContains("diffusion"))
        #expect(appState.currentPromptContextPreviews.joined(separator: " ").localizedCaseInsensitiveContains("pipeline was most fragile") == false)
        #expect(client.stageAPrompts.last?.hasPrefix("CURRENT QUESTION TO ANSWER:") == true)
        #expect(retrieval.queries.suffix(2).map(\.question) == [first.questionText, second.questionText])
    }

    @Test
    func promptSnapshotFrozenEvenIfLatestQuestionMutatesDuringGeneration() async throws {
        let client = ContextIsolationLLMClient()
        client.stageADelayNanoseconds = 250_000_000
        let (appState, _, session) = try makeAppState(client: client)
        let frozen = try saveQuestion(
            "Why did the diffusion-based policy perform better than the autoregressive policy?",
            sessionID: session.id,
            repository: appState.suggestionRepository,
            suffix: "frozen"
        )
        let later = try saveQuestion(
            "Why do you want to join our team?",
            sessionID: session.id,
            repository: appState.suggestionRepository,
            suffix: "later"
        )

        let task = Task {
            try await appState.generateSuggestion(for: frozen, session: session, transcript: frozen.questionText, autoGenerated: true)
        }
        defer { task.cancel() }
        try await waitUntil(timeout: 8.0) {
            appState.activeQuestionID == frozen.id
        }

        appState.lastDetectedQuestion = later
        appState.lastDetectedQuestionText = later.questionText

        try await waitUntil(timeout: 8.0) {
            appState.currentSuggestion?.detectedQuestionID == frozen.id
        }

        #expect(client.stageAPrompts.last?.localizedCaseInsensitiveContains(frozen.questionText) == true)
        #expect(client.stageAPrompts.last?.localizedCaseInsensitiveContains(later.questionText) == false)
        #expect(appState.currentSuggestion?.questionText == frozen.questionText)
    }

